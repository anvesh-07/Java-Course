package OOPS.Array.problems;

class ArrayLength {
  public static void main(String[] args) {
    int nums[] = { 10, 20, 30, 40, 50 };

    System.out.println("Length of array: " + nums.length);
  }
}

/*
 * Explanation:
 * - In Java, arrays have a property `.length` which gives the total number of
 * elements.
 * - Here nums.length = 5
 */
